#include "internal/ti42_internal.h"

int32_t	ti42_getflag(const uint8_t name) {
	if (!in_range(name, 1, TI42_BOOLEAN_CAPS))
		return TI42_NOT_BOOL;
	return caps.boolean[name - 1];
}
