#include "internal/ti42_internal.h"

const char	*ti42_getstr(const uint16_t name) {
	if (!in_range(name ^ 0x8000, 1, TI42_STRING_CAPS))
		return TI42_NOT_STR;
	return caps.string[(name ^ 0x8000) - 1];
}
