#include "internal/ti42_internal.h"

int32_t	ti42_getnum(const uint8_t name) {
	if (!in_range(name ^ 0x80, 1, TI42_NUMERIC_CAPS))
		return TI42_NOT_NUM;
	return caps.numeric[(name ^ 0x80) - 1]; 
}
